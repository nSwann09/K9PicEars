/* Send a command */
void SSD1306_Cmd(int address, char wData) {
    I2C_Write8(address, 0x00, wData); // Send the address of the display
}

/* Envia um dado */
void SSD1306_Data(int address, char wData) {
    I2C_Write8(address, 0xC0, wData); // Send the address of the display
}/* Envia um dado */

void SSD1306_Init(int address) {

    SSD1306_Cmd(address, SSD1306_DISPLAYOFF); // Display Off
    SSD1306_Cmd(address, SSD1306_SETDISPLAYCLOCKDIV); // 0xD5
    SSD1306_Cmd(address, 0x80); // the suggested ratio 0x80 

    /* VERY IMPORTANT!!! */
    SSD1306_Cmd(address, SSD1306_SETMULTIPLEXRATIO); // Multiplex Ratio
    SSD1306_Cmd(address, 0x3F);

    SSD1306_Cmd(address, SSD1306_SETDISPLAYOFFSET); // Display Offset
    SSD1306_Cmd(address, 0x00);
    SSD1306_Cmd(address, SSD1306_SETSTARTLINE); // Display Start Line
    SSD1306_Cmd(address, SSD1306_SEGREMAP | 1);
    SSD1306_Cmd(address, SSD1306_COMSCANDEC); // COM Output Scan Direction
    SSD1306_Cmd(address, SSD1306_SETCOMPINS); // COM Pins Hardware Configuration
    SSD1306_Cmd(address, 0x12);
    SSD1306_Cmd(address, SSD1306_SETCONTRAST); // Contrast Control
    SSD1306_Cmd(address, 255);
    SSD1306_Cmd(address, SSD1306_ENTIREDISPLAYON_RESUME); // Entire Display On / Off
    SSD1306_Cmd(address, SSD1306_NORMALDISPLAY); // Normal / Inverse Display
    SSD1306_Cmd(address, SSD1306_SETDISPLAYCLOCKDIV); // Display Clock Divide Ratio / Frequency Oscillator
    SSD1306_Cmd(address, 0x80);

    SSD1306_Cmd(address, SSD1306_SETPRECHARGE); // Pre-Charge Period
    SSD1306_Cmd(address, 0x25);
    SSD1306_Cmd(address, SSD1306_SETVCOMDETECT); // VCOMH Deselect Level
    SSD1306_Cmd(address, 0x20);

    /* VERY IMPORTANT!!! */
    SSD1306_Cmd(address, SSD1306_PAGEADDR);
    SSD1306_Cmd(address, 0);
    SSD1306_Cmd(address, 7);

    /* Initial cursor position */
    SSD1306_Cmd(address, 0xB0);
    SSD1306_Cmd(address, 0x10);
    SSD1306_Cmd(address, 0x00);
    SSD1306_Cmd(address, SSD1306_MEMORYMODE);
    SSD1306_Cmd(address, 2);

    SSD1306_Cmd(address, SSD1306_CHARGEPUMP); // Charge Pump
    SSD1306_Cmd(address, 0x14); // Enable Charge Pump    
    SSD1306_Cmd(address, SSD1306_DISPLAYON); // Display On
}

/* Set the cursor position */
void SSD1306_Set_Cursor(int address, int row, int col) {
    SSD1306_Cmd(address, SSD1306_PAGESTARTADDR | row); // Set the position of the line
    SSD1306_Cmd(address, (SSD1306_SETHIGHCOLUMN | (col >> 4))); // Set the MSB nibble the column position
    SSD1306_Cmd(address, (0x0f & col)); // Difne LSB nibble the column position
}

void SSD1306_ChrMine(int address, int wChar, char color) {
    char i;
    color = color ? 0x00 : 0xFF;
    for (i = 0; i < 8; i++)
        SSD1306_Data(address, SSD1306_FontMine[wChar][i] ^ color);
}

/* Escreve um texto com caracteres 8x8 na posição especificada */
void SSD1306_OutMine(int address, int row, int col, int texto, char color) {
    SSD1306_Set_Cursor(address, row, col << 3);
    SSD1306_ChrMine(address, texto, color);
}

void SSD1306_TextFill(int address, int TopX, int TopY, int BottomX, int BottomY, char color, int textto) {
    // make this fill TOPL-TOPY to BOTTOML-BOTTOMY
    int x = TopX;
    int y = TopY;
    while (y < BottomY) {
        while (x < BottomX) {
            SSD1306_OutMine(address, y, x, textto, color);
            SSD1306_OutMine(0x78, y, x, textto, color);
            x++;
        }
        x = TopX;
        y++;
    }
}

/* Preeche display with byte */
void SSD1306_Fill(int address, int TopX, int TopY, int BottomX, int BottomY, char color) {
    // make this fill TOPL-TOPY to BOTTOML-BOTTOMY
    SSD1306_TextFill(address, TopX, TopY, BottomX, BottomY, color, 0);
}/* Preeche display with byte */

